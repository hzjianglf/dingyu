package net.risesoft.soa.info.model;

import java.util.Date;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity(name="InformationIndex")
@Table(name="info_main_index")
public class InformationIndex
{

  @Id
  private String id;
  private String infoID;
  private String title;
  private String tableName;
  private String module;
  private String infoType;
  private String infoHtmlType;
  private String creater;
  private String createrName;
  private String dn;
  private Date createTime;
  private Date updateTime;
  private String instance;

  public String getId()
  {
    return this.id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getInfoID() {
    return this.infoID;
  }

  public void setInfoID(String infoID) {
    this.infoID = infoID;
  }

  public String getTitle() {
    return this.title;
  }

  public void setTitle(String title) {
    this.title = title;
  }

  public String getTableName() {
    return this.tableName;
  }

  public void setTableName(String tableName) {
    this.tableName = tableName;
  }

  public String getModule() {
    return this.module;
  }

  public void setModule(String module) {
    this.module = module;
  }

  public String getInfoType() {
    return this.infoType;
  }

  public void setInfoType(String infoType) {
    this.infoType = infoType;
  }

  public String getInfoHtmlType() {
    return this.infoHtmlType;
  }

  public void setInfoHtmlType(String infoHtmlType) {
    this.infoHtmlType = infoHtmlType;
  }

  public String getCreater() {
    return this.creater;
  }

  public void setCreater(String creater) {
    this.creater = creater;
  }

  public String getCreaterName() {
    return this.createrName;
  }

  public void setCreaterName(String createrName) {
    this.createrName = createrName;
  }

  public String getDn() {
    return this.dn;
  }

  public void setDn(String dn) {
    this.dn = dn;
  }

  public Date getCreateTime() {
    return this.createTime;
  }

  public void setCreateTime(Date createTime) {
    this.createTime = createTime;
  }

  public Date getUpdateTime() {
    return this.updateTime;
  }

  public void setUpdateTime(Date updateTime) {
    this.updateTime = updateTime;
  }

  public String getInstance() {
    return this.instance;
  }

  public void setInstance(String instance) {
    this.instance = instance;
  }
}