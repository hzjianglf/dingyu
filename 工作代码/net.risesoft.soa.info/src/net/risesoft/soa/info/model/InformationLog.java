package net.risesoft.soa.info.model;

import java.util.Date;
import javax.persistence.Basic;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity(name="InformationLog")
@Table(name="info_main_log")
public class InformationLog
{

  @Id
  private String id = null;

  @Basic(optional=true)
  private String title = null;

  @Basic(optional=true)
  private String business = null;

  @Basic(optional=true)
  private String businessDesc = null;

  @Basic(optional=true)
  private String instance = null;

  @Basic(optional=true)
  private String creater = null;

  @Basic(optional=true)
  private String createrID = null;

  @Basic(optional=true)
  private String createrDept = null;

  @Basic(optional=true)
  private String remoteAddr = null;

  @Basic(optional=true)
  private Date logTime = null;

  public final String getId() {
    return this.id;
  }

  public final void setId(String id) {
    this.id = id;
  }

  public final String getTitle() {
    return this.title;
  }

  public final void setTitle(String title) {
    this.title = title;
  }

  public final String getBusiness() {
    return this.business;
  }

  public final void setBusiness(String business) {
    this.business = business;
  }

  public final String getBusinessDesc() {
    return this.businessDesc;
  }

  public final void setBusinessDesc(String businessDesc) {
    this.businessDesc = businessDesc;
  }

  public final String getInstance() {
    return this.instance;
  }

  public final void setInstance(String instance) {
    this.instance = instance;
  }

  public final String getCreater() {
    return this.creater;
  }

  public final void setCreater(String creater) {
    this.creater = creater;
  }

  public final String getCreaterID() {
    return this.createrID;
  }

  public final void setCreaterID(String createrID) {
    this.createrID = createrID;
  }

  public final String getCreaterDept() {
    return this.createrDept;
  }

  public final void setCreaterDept(String createrDept) {
    this.createrDept = createrDept;
  }

  public final String getRemoteAddr() {
    return this.remoteAddr;
  }

  public final void setRemoteAddr(String remoteAddr) {
    this.remoteAddr = remoteAddr;
  }

  public final Date getLogTime() {
    return this.logTime;
  }

  public final void setLogTime(Date logTime) {
    this.logTime = logTime;
  }
}