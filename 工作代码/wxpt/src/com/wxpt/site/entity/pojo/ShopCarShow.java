package com.wxpt.site.entity.pojo;

public class ShopCarShow {
	private String show;
	private int shopSum;
	private int shopMoney;
	public String getShow() {
		return show;
	}
	public void setShow(String show) {
		this.show = show;
	}
	public int getShopMoney() {
		return shopMoney;
	}
	public void setShopMoney(int shopMoney) {
		this.shopMoney = shopMoney;
	}
	public int getShopSum() {
		return shopSum;
	}
	public void setShopSum(int shopSum) {
		this.shopSum = shopSum;
	}
	
}
